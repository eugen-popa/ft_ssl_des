#include "libft.h"

char	*ft_strndup(const char *s, int n)
{
	char *s2;

	if (!s)
		return (NULL);
	if (n < 1)
		n = ft_strlen(s);
	s2 = ft_strnew(n);
	ft_strncpy(s2, s, n);
	return (s2);
}
