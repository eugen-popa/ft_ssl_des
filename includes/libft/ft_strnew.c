#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *dst;

	dst = (char *)ft_memalloc(size + 1);
	if (dst)
	{
		ft_memset(dst, '\0', size + 1);
		return (dst);
	}
	return (NULL);
}
