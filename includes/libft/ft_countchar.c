#include "libft.h"

size_t	ft_countchar(const char *s, char c)
{
	size_t i;
	size_t nb;

	i = 0;
	nb = 0;
	if (s && c)
	{
		while (s[i])
		{
			if (s[i++] == c)
				nb++;
		}
	}
	return (nb);
}
