#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*l_next;

	if (!lst || !f)
		return ;
	l_next = lst->next;
	f(lst);
	ft_lstiter(l_next, f);
}
